    private void abrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_abrirActionPerformed
        // TODO add your handling code here:
        JFileChooser dlg = new JFileChooser();
        int resp = dlg.showOpenDialog(this);
        if( resp == JFileChooser.APPROVE_OPTION) {
            try{
                File f = dlg.getSelectedFile();
                BufferedImage img = ImageIO.read(f);
                VentanaInterna vi = new VentanaInterna();
                vi.getLienzo2D().setImage(img);
                this.escritorio.add(vi);
                vi.setTitle(f.getName());
                vi.setVisible(true);
            }catch(Exception ex){
                System.err.println("Error al leer la imagen");
            }
        }
    }//GEN-LAST:event_abrirActionPerformed

    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        // TODO add your handling code here:
        VentanaInterna vi=(VentanaInterna) escritorio.getSelectedFrame();
        if (vi != null) {
            BufferedImage img = vi.getLienzo2D().getPaintedImage();
            if (img != null) {
                JFileChooser dlg = new JFileChooser();
                FileNameExtensionFilter filter = new FileNameExtensionFilter("Archivos de imagen", "jpg", "png"); // Filtra por tipos de imagen comunes
                dlg.setFileFilter(filter);

                int resp = dlg.showSaveDialog(this);
                if (resp == JFileChooser.APPROVE_OPTION) {
                    try {
                        File f = dlg.getSelectedFile();
                        String ext = "";
                        String fileName = f.getName();
                        int i = fileName.lastIndexOf('.');
                        if (i > 0 && i < fileName.length() - 1) {
                            ext = fileName.substring(i + 1).toLowerCase(); // Obtener la extensión del archivo seleccionado
                        }
                        // Verificar si la extensión coincide con la de los formatos que soportan canal alfa
                        if (ext.equals("png")) {
                            ImageIO.write(img, ext, f); // Utilizar la extensión PNG
                        } else {
                            // Si la extensión no es compatible con canal alfa, guardamos como JPG
                            ImageIO.write(img, "jpg", f);
                        }
                        vi.setTitle(f.getName());
                    } catch (Exception ex) {
                        System.err.println("Error al guardar la imagen");
                    }
                }
            }
        }
    }//GEN-LAST:event_guardarActionPerformed

