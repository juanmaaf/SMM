    private float[] crearFiltroCometa(int tam){
        float[] filtro = new float[tam];
        int centro = tam / 2;

        // Asignamos pesos decrecientes a medida que nos alejamos del centro
        for (int i = 0; i < tam; i++) {
            int distancia = i - centro;
            // Damos menos peso a los píxeles más alejados del central, teniendo en cuenta únicamente los pixeles situados a la derecha. Distancia >= 0
            if (distancia >= 0) {
                filtro[i] = 1.0f / (distancia + 1);
            } else {
                filtro[i] = 0.0f;
            }
        }

        // Normalizamos los pesos para asegurarnos de que sumen 1
        float sum = 0;
        for (int i = 0; i < tam; i++) {
            sum += filtro[i];
        }
        for (int i = 0; i < tam; i++) {
            filtro[i] /= sum;
        }

        return filtro;
    }
    
