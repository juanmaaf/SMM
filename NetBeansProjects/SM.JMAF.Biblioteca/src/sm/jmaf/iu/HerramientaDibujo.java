package sm.jmaf.iu;

/**
 * Enumeración que representa las herramientas de dibujo disponibles en la aplicación.
 * Las herramientas incluidas son LINEA, RECTANGULO, ELIPSE y FANTASMA.
 * 
 * Cada herramienta de dibujo corresponde a un tipo específico de figura que el usuario
 * puede dibujar en el lienzo de la aplicación.
 * 
 * @author juanmaaf
 */
public enum HerramientaDibujo {
    LINEA,
    RECTANGULO,
    ELIPSE,
    FANTASMA
}